package br.com.investimentos.financas;

public class ProjecoesFinanceiras {

    private double receitaProjetada;
    private double lucroProjetado;
    private double crescimentoEsperado;
    private double margemLucroEsperada;
    private double custoOperacionalProjetado;
    private double investimentoPrevisto;
    private double retornoEsperado;
    private double valorPresenteLiquido;
    private double taxaInternaRetorno;

    public double getReceitaProjetada() {
        return receitaProjetada;
    }

    public void setReceitaProjetada(double receitaProjetada) {
        this.receitaProjetada = receitaProjetada;
    }

    public double getLucroProjetado() {
        return lucroProjetado;
    }

    public void setLucroProjetado(double lucroProjetado) {
        this.lucroProjetado = lucroProjetado;
    }

    public double getCrescimentoEsperado() {
        return crescimentoEsperado;
    }

    public void setCrescimentoEsperado(double crescimentoEsperado) {
        this.crescimentoEsperado = crescimentoEsperado;
    }

    public double getMargemLucroEsperada() {
        return margemLucroEsperada;
    }

    public void setMargemLucroEsperada(double margemLucroEsperada) {
        this.margemLucroEsperada = margemLucroEsperada;
    }

    public double getCustoOperacionalProjetado() {
        return custoOperacionalProjetado;
    }

    public void setCustoOperacionalProjetado(double custoOperacionalProjetado) {
        this.custoOperacionalProjetado = custoOperacionalProjetado;
    }

    public double getInvestimentoPrevisto() {
        return investimentoPrevisto;
    }

    public void setInvestimentoPrevisto(double investimentoPrevisto) {
        this.investimentoPrevisto = investimentoPrevisto;
    }

    public double getRetornoEsperado() {
        return retornoEsperado;
    }

    public void setRetornoEsperado(double retornoEsperado) {
        this.retornoEsperado = retornoEsperado;
    }

    public double getValorPresenteLiquido() {
        return valorPresenteLiquido;
    }

    public void setValorPresenteLiquido(double valorPresenteLiquido) {
        this.valorPresenteLiquido = valorPresenteLiquido;
    }

    public double getTaxaInternaRetorno() {
        return taxaInternaRetorno;
    }

    public void setTaxaInternaRetorno(double taxaInternaRetorno) {
        this.taxaInternaRetorno = taxaInternaRetorno;
    }
}
